var funcs = template.FuncMap{
	"shortHash": func(s string) string {
		if len(s) > 12 {
			return s[:12]
		}
		return s
	},
	"humanBytes": humanBytes,
	"depth": func(p string) string {
		return strings.Repeat("../", strings.Count(p, "/"))
	},
	"badge": func(kind string) template.HTML {
		k := template.HTMLEscapeString(kind)
		return template.HTML(`<span class="badge ` + k + `">` + k + `</span>`)
	},
	"sevBadge": func(sev string) template.HTML {
		s := template.HTMLEscapeString(sev)
		return template.HTML(`<span class="sev sev-` + s + `">` + s + `</span>`)
	},
}

func humanBytes(n int64) string {
	const unit = 1024
	if n < unit {
		return fmt.Sprintf("%d B", n)
	}
	div, exp := int64(unit), 0
	for x := n / unit; x >= unit; x /= unit {
		div *= unit
		exp++
	}
	return fmt.Sprintf("%.1f %cB", float64(n)/float64(div), "KMGTPE"[exp])
}

