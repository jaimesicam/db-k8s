func mustParse(name, body string) *template.Template {
	t, err := template.New(name).Funcs(funcs).Parse(baseHeader + body + baseFooter)
	if err != nil {
		panic(err)
	}
	if _, err := t.Parse(findingsFragment); err != nil {
		panic(err)
	}
	return t
}

